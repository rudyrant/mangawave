export const supportedLanguages = ["ka", "en"];

export function getLanguage(value) {
  return supportedLanguages.includes(value) ? value : "ka";
}

export function translate(lang, key, vars = {}) {
  const locale = getLanguage(lang);
  const template = messages[locale][key] ?? messages.en[key] ?? key;
  return template.replace(/\{\{\s*(\w+)\s*\}\}/g, (_, token) => String(vars[token] ?? ""));
}
